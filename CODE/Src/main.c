char *get_day(uint8_t i){
    static char* days[] = {"Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"};
    if(i < 1 || i > 7) return "Err";
    return days[i-1];
}

void number_string(uint8_t num, char* buff){
    if(num < 10){
        buff[0] = '0';
        buff[1] = num + '0';
    }
    else if(num >= 10 && num <= 99){
        buff[0] = (num / 10) + '0';
        buff[1] = (num % 10) + '0';
    }
    else {
        // Handle Error values (like 255 from I2C failure)
        buff[0] = 'E';
        buff[1] = 'R';
    }
}
//hh:mm:ss
char* time_to_string(RTC_time_t *rtc_time){
	static char buf[9];
	buf[2]=':';
	buf[5]=':';

	number_string(rtc_time->hours,buf);
	number_string(rtc_time->minutes,&buf[3]);
	number_string(rtc_time->seconds,&buf[6]);
	buf[8]='\0';
	return buf;
}
//dd//mm//yy

char* date_to_string(RTC_date_t *rtc_date){
    static char buf[9];
    buf[2]='/';
    buf[5]='/';

    number_string(rtc_date->date, buf);
    number_string(rtc_date->month, &buf[3]);
    number_string(rtc_date->year, &buf[6]);
    buf[8]='\0';
    return buf;
}
